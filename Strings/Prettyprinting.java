package Strings;


public class Prettyprinting {
    public static void main(String[] args) {
        float a = 453.1274f;
        System.out.printf("Formatted number is %.2f\n", a);

        System.out.printf("Pi is : %.3f\n", Math.PI);

        System.out.printf("Hello my name is %s and i am %s", "Akanksha","cool");
    }
}
