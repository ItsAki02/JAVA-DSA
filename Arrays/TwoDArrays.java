package Arrays;

import java.util.*;

public class TwoDArrays {
    public static void main(String[] args) {
        try (Scanner sc = new Scanner(System.in)) {
            /*
              1 2 3 
              4 5 6
              7 8 9
             */

             int[][] arr = new int[3][];
             for(int i = 0; i < arr.length; i++){
                for( int j = 0; j < arr[i].length; j ++){
                    arr[i][j] = sc.nextInt();

                }

             }
             }

        }
        
    }
    
 

